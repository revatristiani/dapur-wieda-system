    private void harbelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_harbelActionPerformed
        // TODO add your handling code here:
        JTextField harbelField = (JTextField) evt.getSource();
    String input = harbelField.getText().trim();
    if (!input.matches("\\d+")) {
        JOptionPane.showMessageDialog(null, "Masukkan hanya angka untuk Harga Beli.");
        harbelField.setText("");
    }
    }//GEN-LAST:event_harbelActionPerformed

    private void kd_brgKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_kd_brgKeyPressed
        // TODO add your handling code here:
int tekanenter = evt.getKeyCode();
    if (tekanenter == KeyEvent.VK_ENTER) {
        String kode = kd_brg.getText().trim();

       Model_Barang barang = controller.cariData(kode);

        if (barang != null) {
            nm_brg.setText(barang.getNama());
            stock.setText(String.valueOf(barang.getStock()));
            harbel.setText(String.valueOf(barang.getHargaBeli()));
            harjul.setText(String.valueOf(barang.getHargaJual()));
            catatan.setText(barang.getDeskripsi());
            JOptionPane.showMessageDialog(null, "Data Ditemukan");
        } else {
            JOptionPane.showMessageDialog(null, "Data Tidak Ditemukan");
        }
    }
    }//GEN-LAST:event_kd_brgKeyPressed

