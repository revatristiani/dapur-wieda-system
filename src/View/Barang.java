    private void harjulActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_harjulActionPerformed
        // TODO add your handling code here:
        JTextField harjulField = (JTextField) evt.getSource();
        String input = harjulField.getText().trim();

    try {
        int hargaJual = Integer.parseInt(input);

        if (hargaJual < 0) {
            JOptionPane.showMessageDialog(null, "Harga jual tidak boleh negatif.");
            harjulField.setText("");
            harjulField.requestFocus();
        }
       
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(null, "Masukkan hanya angka.");
        harjulField.setText("");
        harjulField.requestFocus();
    }
    }//GEN-LAST:event_harjulActionPerformed

