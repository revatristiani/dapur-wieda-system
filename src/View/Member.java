    private void kd_mberKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_kd_mberKeyPressed
        // TODO add your handling code here:

         if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
        String id = kd_mber.getText().trim();
        try {
            ResultSet rs = controller.cariMemberById(id); // ← panggil method dari controller
            if (rs != null && rs.next()) {
                nama.setText(rs.getString("nama"));
                no_tlp.setText(rs.getString("no_tlp"));
                alamat.setText(rs.getString("alamat"));
                txtEmail.setText(rs.getString("email"));
                JOptionPane.showMessageDialog(this, "Data Ditemukan");
            } else {
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_kd_mberKeyPressed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        String km = kd_mber.getText().trim();
    String nm = nama.getText().trim();
    String tlp = no_tlp.getText().trim();
    String almt = alamat.getText().trim();
    String email = txtEmail.getText().trim();

    if (km.isEmpty() || nm.isEmpty() || tlp.isEmpty() || almt.isEmpty() || email.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Harap lengkapi semua kolom.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    boolean success = controller.insertMember(km, nm, tlp, almt, email);

    if (success) {
        clearForm();
        tampilData();  // refresh tabel
        JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
        kd_mber.setText(controller.generateNewNumber());
    } else {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan data");
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String id = kd_mber.getText().trim();
        String nm = nama.getText().trim();
        String tlp = no_tlp.getText().trim();
        String almt = alamat.getText().trim();
        String email = txtEmail.getText().trim();

        if (id.isEmpty() || nm.isEmpty() || tlp.isEmpty() || almt.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Harap lengkapi semua kolom.");
            return;
        }

        if (!controller.isDataExists(id)) {
            JOptionPane.showMessageDialog(this, "Data tidak ditemukan.");
            return;
        }

        if (controller.updateMember(id, nm, tlp, almt, email)) {
            tampilData();
            clearForm();
            JOptionPane.showMessageDialog(this, "Data Berhasil Diubah");
            kd_mber.setText(controller.generateNewNumber());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

