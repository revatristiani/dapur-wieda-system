    private void no_tlpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_no_tlpActionPerformed
        // TODO add your handling code here:
        JTextField noTlpField = (JTextField) evt.getSource();
        String input = noTlpField.getText();
        try {
            Long.parseLong(input);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Masukkan hanya angka.");
            noTlpField.setText("");
        }
    }//GEN-LAST:event_no_tlpActionPerformed

